@SuppressWarnings("serial")
public class helmetModelException extends Exception{

	/**
	 * @param args
	 */
	public helmetModelException() {
		super();
	}

	public helmetModelException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public helmetModelException(String message, Throwable cause) {
		super(message, cause);
	}

	public helmetModelException(String message) {
		super(message);
	}

	public helmetModelException(Throwable cause) {
		super(cause);
	}

	
	
}


